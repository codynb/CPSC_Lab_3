import turtle
def square(paint):
    pop = turtle.Turtle()
    pop.color(paint)
    pop.forward(100)
    pop.right(90)
    pop.forward(100)
    pop.right(90)
    pop.forward(100)
    pop.right(90)
    pop.forward(100)
square("red")
square('cyan')
square('orange')
square('purple')
square('gold')
